
var ginKg = 300;
var kg = prompt("Ինչքա՞ն կիլոգրամ խնձոր եք ցանկանում գնել:");

//գրում ենք պայման, որպեսզի մուտքագրվի դրական թիվ, &&-դատարկ չլինի
if (!isNaN(kg) && kg > 0) {
    var money = kg * ginKg;
    alert(`Դուք պետք է վճարեք ${money} դրամ։`); //${...}-փոփոխականներին տեքստի մեջ նեառնելու համար
} else {
    alert("Խնդրում ենք մուտքագրել ճիշտ թիվ։");
}