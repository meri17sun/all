
var qash = prompt("Մուտքագրեք ձեր քաշը կիլոգրամով:");

if (qash && !isNaN(qash)) {

  var jur = qash * 0.03;

  alert(`Ձեզ անհրաժեշտ է մոտավորապես ${jur.toFixed(2)} լիտր ջուր օրական խմել։`);
} else {
  alert("Խնդրում ենք մուտքագրել վավեր թիվ քաշի համար։");
}
//to.Fixed-ը կլորացնում է թիվը մինչև 2 նիշ